// Generates lib/work-images.ts from /public/Stationary Design plus the newer
// /public/Website Data drop. Run with:  node scripts/gen-work-images.mjs
import { readdirSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const PUBLIC = join(process.cwd(), "public");
const IMG = /\.(jpe?g|png|webp|gif|avif)$/i;

const listImages = (dir, urlPrefix) =>
  readdirSync(dir)
    .filter((f) => IMG.test(f))
    .sort()
    .map((f) => `${urlPrefix}/${f}`);

const listFolders = (root) => {
  try {
    return readdirSync(root).filter((f) => {
      try { return statSync(join(root, f)).isDirectory(); } catch { return false; }
    });
  } catch { return []; }
};

const manifest = {};
const add = (key, imgs) => {
  manifest[key] = [...(manifest[key] ?? []), ...imgs];
};

// 1) Original library under /public/Stationary Design (folder name == manifest key).
const STATIONARY = join(PUBLIC, "Stationary Design");
for (const folder of listFolders(STATIONARY).sort()) {
  add(folder, listImages(join(STATIONARY, folder), `/Stationary Design/${folder}`));
}

// 2) Newer drop under /public/Website Data — map each folder to its manifest key.
const WEBSITE_DATA = join(PUBLIC, "Website Data");
const WEBSITE_DATA_MAP = {
  "Bag Design": "Bag Design",
  "Banner & Standee": "banner design",
  "Brochure Design": "Brouchers & File",
  "Flyer Design": "Flyer Design",
  "Invitation Card Design": "invtations",
  "Menu Design": "menu",
  "Packaging & Label Design": "packaging",
  "Social Media Post": "Social Media Post",
  "Stationary Design": "Stationary Design",
  "Tag Design": "tag design",
};
for (const folder of listFolders(WEBSITE_DATA).sort()) {
  const key = WEBSITE_DATA_MAP[folder] ?? folder;
  add(key, listImages(join(WEBSITE_DATA, folder), `/Website Data/${folder}`));
}

const body = Object.entries(manifest)
  .map(([folder, imgs]) => {
    const list = imgs.map((p) => `    ${JSON.stringify(p)},`).join("\n");
    return `  ${JSON.stringify(folder)}: [\n${list}\n  ],`;
  })
  .join("\n");

const out = `// AUTO-GENERATED by scripts/gen-work-images.mjs — do not edit by hand.
// Images under /public/Stationary Design and /public/Website Data, grouped by category.
export const workImages: Record<string, string[]> = {
${body}
};
`;

writeFileSync(join(process.cwd(), "lib", "work-images.ts"), out);
const total = Object.values(manifest).reduce((n, a) => n + a.length, 0);
console.log(`Wrote lib/work-images.ts — ${Object.keys(manifest).length} categories, ${total} images.`);
